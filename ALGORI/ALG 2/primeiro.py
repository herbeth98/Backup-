#1) Escrever um programa que leia um valor em metros e exiba convertido em centímetros e milímetros.

print("Primeira Questão")

metros = float (input( "Valor em Metros: "))

centimetros= float(metros * 100)

milimetros= float(metros * 1000)

print ('Valor em Centímetros: %s,  | Valor em Milímetros: %s ' %(centimetros,milimetros)   )

